def print_params(a=1, b='строка', c=True):
    print(f"a: {a}, b: {b}, c: {c}")

# Вызов функции с разным количеством аргументов
print_params()                      # Все значения по умолчанию
print_params(10)                   # Изменён только 'a'
print_params(10, 'новая строка')   # Изменены 'a' и 'b'
print_params(b=25)                 # Изменён только 'b'
print_params(c=[1, 2, 3])          # Изменён только 'c'

# Создание списка и словаря
values_list = [42, 'спец. строка', False]  # Список с 3 элементами разных типов
values_dict = {'a': 99, 'b': 'другая строка', 'c': False}  # Словарь с тремя ключами

# Передача значений в функцию с распаковкой
print_params(*values_list)         # Распаковка списка
print_params(**values_dict)        # Распаковка словаря

# Новый список с двумя элементами
values_list_2 = [100, 'еще одна строка']

# Вызов функции с распаковкой и дополнительным параметром
print_params(*values_list_2, 42)  # Последний параметр - 42
